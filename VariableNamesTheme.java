public class VariableNamesTheme {
    public static void main(String[] args) {
        System.out.println("1. Разные переменные" + "\n");
        
        // Цифра
        int number = 5;
        System.out.println("Цифра " + number);

        // Сумма чисел
        int numbersSum = 10;
        System.out.println("Сумма чисел = " + numbersSum);

        // Произведение цифр
        int digitsMult = 25;
        System.out.println("Произведение цифр = " + digitsMult);

        // Максимальное число
        int maxNumber = 45;
        System.out.println("Максимальное число " + maxNumber);

        // Количество десятков
        int tensCount = 3;
        System.out.println("Количество десятков " + tensCount);

        // Вес собаки
        int dogWeight = 70;
        System.out.println("Вес собаки " + dogWeight);

        // Исходное число
        int originalNumber = 1;
        System.out.println("Исходное число " + originalNumber);

        // Процент по вкладу
        int depositInteres = 13;
        System.out.println("Процент по вкладу " + depositInteres);

        // Переменная хранит символ &
        char ampersand = '&';
        System.out.println("Переменная хранит символ " + ampersand);

        // Код ошибки
        String errorCode = "509 Bandwidth Limit Exceeded";
        System.out.println("Код ошибки " + errorCode);

        // Тип сообщения
        String messageType = "SMS";
        System.out.println("Тип сообщения " + messageType);

        // Число нулей
        int zerosOfNumber = 20;
        System.out.println("Число нулей " + zerosOfNumber);

        // Уникальное число
        int uniqueNumber = 93;
        System.out.println("Уникальное число " + uniqueNumber);

        // Случайное число
        int randomNumber = 48;
        System.out.println("Случайное число " + randomNumber);

        // Математическое выражение
        int mathExpression = 2 + 3;
        System.out.println("Математическое выражение " + mathExpression);

        // Счет в игре
        String gameScore = "2 : 3";
        System.out.println("Счет в игре " + gameScore);

        // Максимальная длина
        int maxLength = 400;
        System.out.println("Максимальная длина " + maxLength);

        // Пункт меню
        String menuItem = "Овощи";
        System.out.println("Пункт меню " + menuItem);

        // Стоимость кофе
        int coffeePrice = 1;
        System.out.println("Стоимость кофе " + coffeePrice);

        // Дата начала
        String startDate = "12.12.2012";
        System.out.println("Дата начала " + startDate);

        // Окончание диапазона
        int rangeEnd = 0;
        System.out.println("Окончание диапазона " + rangeEnd);

        // Полное имя работника месяца
        String employeMonthFullName = "Иванов Иван";
        System.out.println("Полное имя работника месяца " + employeMonthFullName);

        // Название электронной книги
        String ebookTitle = "Francmasoneria";
        System.out.println("Название электронной книги " + ebookTitle);

        // Размер
        int size = 42;
        System.out.println("Размер " + size);

        // Вместимость
        int capacity = 99;
        System.out.println("Вместимость " + capacity);

        // Счетчик
        double counter = 3.25d;
        System.out.println("Счетчик " + counter);

        // Путь до файла
        String filePath = "C:/Users/username/Documents ";
        System.out.println("Путь до файла " + filePath);

        // Количество чисел в каждой строке
        int numbersCountPerLine = 6;
        System.out.println("Количество чисел в каждой строке " + numbersCountPerLine + "\n");

        System.out.println("2. Boolean-переменные" + "\n");
        
        // Сотни равны?
        int x1 = 200;
        int x2 = 200;
        boolean hasHundredsEquals = x1 == x2;
        System.out.println("Сотни равны? " + hasHundredsEquals);

        // Компьютер включен?
        boolean isPcOn = true;
        System.out.println("Компьютер включен? " + isPcOn);

        // Есть равные цифры?
        int x = 3;
        int y = 5;
        boolean hasEqualDigits = x == y;
        System.out.println("Есть равные цифры? " + hasEqualDigits);

        // Создано?
        boolean isCreated = false;
        System.out.println("Создано? " + isCreated);

        // Пустое?
        boolean isEmpty = false;
        System.out.println("Пустое? " + isEmpty);

        // Активное?
        boolean isActive = true;
        System.out.println("Активное? " + isActive);

        // Новое?
        boolean isNew = true;
        System.out.println("Новое? " + isNew);

        // Электронная почта действительная?
        String text = "Электронная почта действительная?";
        boolean isValidEmail = true;
        System.out.println(text + " " + isValidEmail);

        // Имеются уникальные строки?
        String strings = "Имеются уникальные строки?";
        boolean hasUniqueStrings = false;
        System.out.println(strings + " " + hasUniqueStrings + "\n");

        System.out.println("3. Аббревиатуры" + "\n");

        // Старый universally unique identifier
        String oldUuid = "Старый UUID: 047c0458-46c2-4f3b-945a-d063ab2c0bef";
        System.out.println(oldUuid);

        // Производитель оперативной памяти
        String ramManufactur = "Corsair";
        System.out.println("Производитель ОЗУ: " + ramManufactur);

        // Ёмкость жесткого диска
        String hdCapaciti = "256 ГБ";
        System.out.println("Ёмкость CD: " + hdCapaciti);

        // Протокол передачи гипертекста
        String http = "http://ya.ru";
        System.out.println("http: " + http);

        // Сокращенный uniform resource locator
        String shortUrl = "https://vk.cc/clFVUP";
        System.out.println("Сокращенный URL: " + shortUrl);

        // Новый идентификатор клиента
        String newId = "V-cN-3545";
        System.out.println("Новый ID: " + newId);

        // American standard code for information interchange
        char ascii = '$';
        System.out.println("ASCII: " + ascii);
    }
} 